export class I18n {
  constructor(defaultLocale = "en") {
    this.defaultLocale = defaultLocale;
    this.locale = defaultLocale;
    this.select = null;
  }

  init() {
    const language = (navigator.language || "").toLowerCase();
    const timeZone = Intl.DateTimeFormat().resolvedOptions().timeZone || "";
    const isSpanishZone =
      /^(America\/Bogota|America\/Mexico_City|America\/Lima|America\/Santiago|America\/Buenos_Aires|America\/Montevideo|America\/Caracas|America\/Guayaquil|America\/La_Paz|Europe\/Madrid)$/.test(
        timeZone
      );
    if (language.startsWith("es") || isSpanishZone) {
      this.locale = "es";
    } else {
      this.locale = this.defaultLocale;
    }
    this.select = document.getElementById("language-select");
    if (this.select) {
      this.select.value = this.locale;
      this.select.addEventListener("change", (event) => {
        this.setLocale(event.target.value);
      });
    }
    this.apply();
  }

  setLocale(locale) {
    this.locale = translations[locale] ? locale : this.defaultLocale;
    if (this.select && this.select.value !== this.locale) {
      this.select.value = this.locale;
    }
    this.apply();
    window.dispatchEvent(new CustomEvent("app:localeChanged", { detail: { locale: this.locale } }));
  }

  t(key, params = {}) {
    const map = translations[this.locale] || translations[this.defaultLocale];
    const text = map[key] || translations[this.defaultLocale][key] || key;
    return Object.entries(params).reduce(
      (acc, [paramKey, paramValue]) => acc.replaceAll(`{${paramKey}}`, String(paramValue)),
      text
    );
  }

  apply() {
    document.documentElement.lang = this.locale;
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach((node) => {
      const key = node.getAttribute("data-i18n");
      node.textContent = this.t(key);
    });
  }
}
